package com.cs169.letscarpool;

public class SearchResultDetails {

    int icon ;
    String username;
    String map_info;
    String depart_time;
    String aveg_rating;
    
	public int getIcon() {
		return icon;
	}
	public void setIcon(int icon) {
		this.icon = icon;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getMapInfo() {
		return map_info;
	}
	public void setMapInfo(String map_info) {
		this.map_info = map_info;
	}
	public String getDepartTime() {
		return depart_time;
	}
	public void setDepartTime(String depart_time) {
		this.depart_time = depart_time;
	}
	public String getAverageRating() {
		return aveg_rating;
	}
	public void setAverageRating(String aveg_rating) {
		this.aveg_rating = aveg_rating;
	}

}